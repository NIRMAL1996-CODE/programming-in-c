#include<stdio.h>
int main()
{
    char s[100] , ch; 
    int count=0; 
    printf("Enter the string\n");
    fgets(s,100,stdin); 
    
     printf("Enter a character : ");
     scanf("%c",&ch); 

     //create a loop to count the frequency of such character asked by user
      for(int i=0; s[i]!='\0'; i++) //write s[i] denotes to string's characters one by one
      {
        if(ch==s[i])
        count++;
      }
        printf("count of %c = %d",ch,count);
      
      return 0;
}